public class Carro{

    private String marca;
    private String cor;
    private double valor;

    public Carro(){

    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(int marca) {
        this.marca = marca;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(int cor) {
        this.cor = cor;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }


}